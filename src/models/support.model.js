import mongoose from "mongoose";

const supportMessageSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      trim: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export const SupportMessage = mongoose.model(
  "SupportMessage",
  supportMessageSchema
);
